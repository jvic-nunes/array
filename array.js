let gaveteiro = ["joao", "eduardo", "dani", "sunki"]
gaveteiro.push("lino")
//push é pra adicionar no final
console.log(gaveteiro)
gaveteiro.pop()
//pop é pra excluir a ultima posição
console.log(gaveteiro)
gaveteiro.shift()
//shift exclui a 1 posi.
console.log(gaveteiro)
gaveteiro.unshift("joao")
//unshift add na 1 posi.
console.log(gaveteiro)



